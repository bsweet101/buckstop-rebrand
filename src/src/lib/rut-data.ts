/**
 * Returns color for a state based on peak rut timing.
 * Uses 7 distinct colors for better visual differentiation on the map.
 */
export function getRutTimingColor(peakRutStart: string): string {
  const month = peakRutStart.split(' ')[0];
  const day = parseInt(peakRutStart.split(' ')[1], 10);

  if (month === 'October') return '#228B22';           // forest green (very distinct)
  if (month === 'November' && day <= 5) return '#6DB33F';   // lime green
  if (month === 'November' && day <= 10) return '#F0C820';  // bright yellow
  if (month === 'November' && day <= 15) return '#E88C20';  // bright orange
  if (month === 'November' && day <= 20) return '#D05020';  // red-orange
  if (month === 'November') return '#A02010';               // crimson
  if (month === 'December') return '#701818';               // dark red
  return '#401010';                                         // very dark red (Jan+)
}

export function getRutTimingLabel(peakRutStart: string): string {
  const month = peakRutStart.split(' ')[0];
  const day = parseInt(peakRutStart.split(' ')[1], 10);

  if (month === 'October') return 'Early (October)';
  if (month === 'November' && day <= 7) return 'Early Nov (1-7)';
  if (month === 'November' && day <= 15) return 'Mid Nov (8-15)';
  if (month === 'November') return 'Late Nov (16-30)';
  if (month === 'December') return 'December';
  return 'Late (January+)';
}


/** Get rut data for a specific state */
export function getStateRutData(stateCode: string): StateRutData | undefined {
  return allStateRutData.find((s) => s.stateCode === stateCode);
}

/** Get all states in a region */
export function getStatesByRegion(region: RutRegion): StateRutData[] {
  return allStateRutData.filter((s) => s.region === region);
}
